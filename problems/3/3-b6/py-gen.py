import random

def generate_data(index: int) -> str:
    """
    生成测试数据，覆盖各种边界情况。
    返回格式: 字符串，如 "12345.67" 或 "0" 或 "1000000000"
    """
    # 场景权重：正常值(60%), 边界值(20%), 零值特例(10%), 非法/极端(10%)
    scenario = random.choices(
        ["normal", "boundary", "zero_case", "extreme"],
        weights=[60, 20, 10, 10]
    )[0]

    if scenario == "boundary":
        # 边界值：0, 100亿-ε, 亿/万位临界, 角分临界
        boundary_values = [
            0, 0.01, 0.03, 0.1, 0.10, 0.30, 0.99,
            1, 1.03, 1.30, 9.03, 9.30, 10.03, 12.30,
            100, 1000, 10000, 100000, 1000000, 10000000,
            100000000, 1000000000, 9999999999.99,
            10001, 10010, 10100, 11000, 100001, 100010,
            100100, 101000, 110000, 1000001, 1000010,
            1000100, 1001000, 1010000, 1100000,
            100000001, 100000010, 100000100, 100001000,
            100010000, 100100000, 101000000, 110000000,
            1000000000.01, 1000000000.10, 9999999999.01,
            8912003005.78, 2501200350.03, 1203056740.01,
            203056740.20, 23000056.82, 3051200.72,
            301000.35, 10001.34, 8070.23, 9876.54,
            803.03, 1099999999, 100001234, 100000000.10,
            100100003.01, 10000010, 1000000100, 101000010
        ]
        return str(random.choice(boundary_values))

    elif scenario == "zero_case":
        # 零的特殊情况：中间零、连续零、末尾零
        return str(random.choice([
            10001, 10010, 10100, 100001, 100010, 100100,
            101000, 1000001, 1000010, 1000100, 1001000,
            1010000, 1100000, 10000001, 10000010, 10000100,
            10001000, 10010000, 10100000, 11000000,
            100001000, 100000100, 1000000010, 100000001,
            100001000, 100000100, 1000000010, 100000001,
            1.03, 10.03, 100.03, 1000.03, 10000.03,
            1.30, 10.30, 100.30, 1000.30, 10000.30,
            0.03, 0.30, 0.07, 0.70
        ]))

    elif scenario == "extreme":
        # 极端值：接近 100 亿、科学计数法边界、浮点精度敏感值
        return str(random.choice([
            9999999999.99, 9999999999.90, 9999999999.09,
            9999999999.00, 9900000000.00, 9000000000.00,
            1000000000.00, 100000000.00, 10000000.00,
            # 浮点精度测试：0.1+0.2 类问题
            0.3, 0.7, 1.2, 2.5
        ]))

    else:  # normal
        # 随机正常值：[0, 100亿)
        if random.random() < 0.3:
            # 30% 概率生成整数
            value = random.randint(0, 9999999999)
            return str(value)
        else:
            # 70% 概率生成带小数的值（精确到分）
            integer_part = random.randint(0, 9999999999)
            decimal_part = random.randint(0, 99)
            return f"{integer_part}.{decimal_part:02d}"